public class Praktikumsstunde {

    public String thema;
    public int Datum;
    public String Uhrzeit;
    public String Raum;
    public String Aufgaben;

    public Praktikumsstunde(String thema, int Datum, String Uhrzeit, String Raum, String Aufgaben) {
        this.thema = thema;
        this.Datum = Datum;
        this.Uhrzeit = Uhrzeit;
        this.Raum = Raum;
        this.Aufgaben = Aufgaben;
    }


}
